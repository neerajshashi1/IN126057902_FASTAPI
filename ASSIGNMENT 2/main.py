    "        \"total_products\":     len(products),\n",
    "        \"in_stock_count\":     len(in_stock),\n",
    "        \"out_of_stock_count\": len(out_stock),\n",
    "        \"most_expensive\":     {\"name\": expensive[\"name\"], \"price\": expensive[\"price\"]},\n",
    "        \"cheapest\":           {\"name\": cheapest[\"name\"],  \"price\": cheapest[\"price\"]},\n",
    "        \"categories\":         categories,\n",
    "    }\n",
