    "@app.get('/products/{product_id}')\n",
    "def get_product(product_id: int):\n",
    "    product = find_product(product_id)\n",
    "    if not product:\n",
    "        return {'error': 'Product not found'}\n",
    "    return {'product': product}\n",
    " \n",
    "# ── Day 2: POST + Pydantic ─────────────────────────────────────────\n",
    "@app.post('/orders')\n",
    "def place_order(order_data: OrderRequest):\n",
