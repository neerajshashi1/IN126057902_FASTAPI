    "# Q4 - Build a Product Summary Dashboard\n",
    "# endpoint: GET /products/summary\n",
    "@app.get(\"/products/summary\")\n",
    "def product_summary():\n",
    "    in_stock   = [p for p in products if     p[\"in_stock\"]]\n",
    "    out_stock  = [p for p in products if not p[\"in_stock\"]]\n",
    "    expensive  = max(products, key=lambda p: p[\"price\"])\n",
    "    cheapest   = min(products, key=lambda p: p[\"price\"])\n",
    "    categories = list(set(p[\"category\"] for p in products))\n",
