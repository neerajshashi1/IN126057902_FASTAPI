    "            failed.append({\"product_id\": item.product_id, \"reason\": f\"{product['name']} is out of stock\"})\n",
    "        else:\n",
    "            subtotal = product[\"price\"] * item.quantity\n",
    "            grand_total += subtotal\n",
    "            confirmed.append({\"product\": product[\"name\"], \"qty\": item.quantity, \"subtotal\": subtotal})\n",
