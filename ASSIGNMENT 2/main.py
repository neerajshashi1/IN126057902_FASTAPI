    "from fastapi import FastAPI, Query\n",
    "from pydantic import BaseModel, Field\n",
    "from typing import Optional, List\n",
    " \n",
    "app = FastAPI()\n",
    "\n",
    "# ══ PYDANTIC MODEL ════════════════════════════════════════════════\n",
    "class OrderRequest(BaseModel):\n",
    "    customer_name:    str = Field(..., min_length=2, max_length=100)\n",
    "    product_id:       int = Field(..., gt=0)\n",
    "    quantity:         int = Field(..., gt=0, le=100)\n",
    "    delivery_address: str = Field(..., min_length=10)\n",
    " \n",
    "# Q3 - Accept Customer Feedback\n",
    "class CustomerFeedback(BaseModel):\n",
    "    customer_name: str            = Field(..., min_length=2, max_length=100)\n",
    "    product_id:   int            = Field(..., gt=0)\n",
    "    rating:       int            = Field(..., ge=1, le=5)\n",
    "    comment:      Optional[str]  = Field(None, max_length=300)\n",
    "\n",
    "# Q5 - Validate & Place a Bulk Order\n",
    "class OrderItem(BaseModel):\n",
    "    product_id: int = Field(..., gt=0)\n",
    "    quantity:   int = Field(..., gt=0, le=50)\n",
    "\n",
    "class BulkOrder(BaseModel):\n",
    "    company_name:  str           = Field(..., min_length=2)\n",
    "    contact_email: str           = Field(..., min_length=5)\n",
    "    items:         List[OrderItem] = Field(..., min_items=1)\n",
    "\n",
    "# ══ DATA ══════════════════════════════════════════════════════════\n",
    "products = [\n",
    "    {'id':1,'name':'Wireless Mouse','price':499,'category':'Electronics','in_stock':True},\n",
    "    {'id':2,'name':'Notebook',      'price': 99,'category':'Stationery', 'in_stock':True},\n",
    "    {'id':3,'name':'USB Hub',        'price':799,'category':'Electronics','in_stock':False},\n",
    "    {'id':4,'name':'Pen Set',         'price': 49,'category':'Stationery', 'in_stock':True},\n",
    "]\n",
    "orders = []\n",
    "feedback = []\n",
    "order_counter = 1\n",
    " \n",
    "# ══ HELPER FUNCTIONS ══════════════════════════════════════════════\n",
    " \n",
    "def find_product(product_id: int):\n",
    "    \"\"\"Search products list by ID. Returns product dict or None.\"\"\"\n",
    "    for p in products:\n",
    "        if p['id'] == product_id:\n",
    "            return p\n",
    "    return None\n",
    " \n",
    "def calculate_total(product: dict, quantity: int) -> int:\n",
    "    \"\"\"Multiply price by quantity and return total.\"\"\"\n",
    "    return product['price'] * quantity\n",
    " \n",
    "def filter_products_logic(category=None, min_price=None,\n",
    "                          max_price=None, in_stock=None):\n",
    "    \"\"\"Apply filters and return matching products.\"\"\"\n",
    "    result = products\n",
    "    if category  is not None:\n",
    "        result = [p for p in result if p['category'] == category]\n",
    "    if min_price is not None:\n",
    "        result = [p for p in result if p['price'] >= min_price]\n",
    "    if max_price is not None:\n",
    "        result = [p for p in result if p['price'] <= max_price]\n",
    "    if in_stock  is not None:\n",
    "        result = [p for p in result if p['in_stock'] == in_stock]\n",
    "    return result\n",
    " \n",
    "# ══ ENDPOINTS ═════════════════════════════════════════════════════\n",
    "# ROUTE ORDER RULE:\n",
    "#   Fixed routes  (/filter, /compare)  must come BEFORE\n",
    "#   Variable route (/products/{product_id})\n",
    "# ═════════════════════════════════════════════════════════════════\n",
    " \n",
    "# ── Day 1 ─────────────────────────────────────────────────────────\n",
    "@app.get('/')\n",
    "def home():\n",
    "    return {'message': 'Welcome to our E-commerce API'}\n",
    " \n",
    "@app.get('/products')\n",
    "def get_all_products():\n",
    "    return {'products': products, 'total': len(products)}\n",
    " \n",
    "# ── Day 2: Query Parameters ────────────────────────────────────────\n",
    "# NOTE: /filter, /compare, /summary must come BEFORE /{product_id}\n",
    "\n",
    "@app.get('/products/filter')\n",
    "def filter_products(\n",
    "    category:  str  = Query(None, description='Electronics or Stationery'),\n",
    "    min_price: int  = Query(None, description='Minimum price'),\n",
    "    max_price: int  = Query(None, description='Maximum price'),\n",
    "    in_stock:  bool = Query(None, description='True = in stock only'),\n",
    "):\n",
    "    result = filter_products_logic(category, min_price, max_price, in_stock)\n",
    "\n",
    "    # Q1 - Filter Products by Minimum Price\n",
    "    if min_price:\n",
    "        result = [p for p in result if p['price'] >= min_price]\n",
    "\n",
    "    if max_price:\n",
    "        result = [p for p in result if p['price'] <= max_price]\n",
    "\n",
    "    return {'filtered_products': result, 'count': len(result)}\n",
    " \n",
    "# ── Day 3: Compare (fixed route — must stay BEFORE /{product_id}) ─\n",
    "@app.get('/products/compare')\n",
    "def compare_products(\n",
    "    product_id_1: int = Query(..., description='First product ID'),\n",
    "    product_id_2: int = Query(..., description='Second product ID'),\n",
    "):\n",
    "    p1 = find_product(product_id_1)\n",
    "    p2 = find_product(product_id_2)\n",
    "    if not p1:\n",
    "        return {'error': f'Product {product_id_1} not found'}\n",
    "    if not p2:\n",
    "        return {'error': f'Product {product_id_2} not found'}\n",
    "    cheaper = p1 if p1['price'] < p2['price'] else p2\n",
    "    return {\n",
    "        'product_1':    p1,\n",
    "        'product_2':    p2,\n",
    "        'better_value': cheaper['name'],\n",
    "        'price_diff':   abs(p1['price'] - p2['price']),\n",
    "    }\n",
